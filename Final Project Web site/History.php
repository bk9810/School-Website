        <p>
          Kandarwa Central College<br>
          Beginning: 1905<br>
          Location: Historic City of Sri Lanka<br>
          <br><br>
          <br>
        </p>

        <p>
          Kanadarwa Central College This school was founded in 1905 by a group of visionary educationists led by Dr. Perera. This school is located in a historic city in Sri Lanka and was established with the aim of providing quality education to children. Initially, this school started as a small institution with only 35 students and 4 teachers in the beginning in an ordinary building.<br>
          <br>
          <br>
        </p>

        <p>
          The school saw significant growth in the 1960s. Brought new opportunities and increased demand for quality education. Also, under the new leadership of the school, the school improved its infrastructure by adding new classrooms, a library and a laboratory. By 1990, the student population had grown to 1000, with an increasingly diverse curriculum that included arts, sciences and sports.<br>
       <br>
       <br>
        </p>

        <p>
          With the introduction of computer science courses in 1992, this school joined the digital era like other schools in Sri Lanka. The school continued to operate, providing a safe haven and stability to its students. Due to disruptions to the school with the country's war, during this period the school's leadership made important decisions such as the importance of community, strengthening the school's support systems and improving security measures.<br>
        <br>
        <br>
        </p>

        <p>
          The school's focus on holistic education has led to the development of state-of-the-art facilities including a new auditorium, sports complex and science department. Now the school's commitment to sustainability and community service makes our school a beacon of excellence in Sri Lankan education. And it has received many evaluations.
        </p>
